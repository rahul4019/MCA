# C-PYTHON
# c-python refers to the default implementation of python programming language, written in the
# c programming language. it is the most widely used implementation of python and serves as the
# reference implementation of the language.

# key features of CPython
# 1. written in C:
#     * cpython is implemented in the c language, which allows it to be efficient and portable.
#     * it compiles python source code into bytecode, which is then executed by the cpython
#       interpretor

# 2. interpreted language:
#   cpython executes python code line by line after converting it to bytecode. this means python
#   programs do not need a separate compilation step like c and java.

# cpython is the interpretor of the python language
